// Helper: get insignia URL for a branch + grade
window.VCP_INSIGNIA.get = function(branch, grade) {
  if (!grade) return null;
  // Officers use same insignia across branches
  if (grade.startsWith('O-') || grade.startsWith('W-')) {
    var off = window.VCP_INSIGNIA._officer;
    // Check Army for warrant officers
    if (grade.startsWith('W-') && window.VCP_INSIGNIA['Army']) {
      return window.VCP_INSIGNIA['Army'][grade] || null;
    }
    return off ? off[grade] : null;
  }
  var branchData = window.VCP_INSIGNIA[branch];
  if (!branchData) return null;
  return branchData[grade] || null;
};
